% function randomHeader () {

    randInt=`expr $(cat /dev/urandom | base64 | tr -dc '0-9' | head -c1) % 4`

    if (( $randInt == 0 ))
    then
        echo " __   __                      __           __     ___       ___     ___                   __        __        __    ___  __   __  ";
        echo "/__\` |__) | |\ | |\ | | |\ | / _\`    |  | |__)     |  |__| |__     |__  |    |  | \_/    /  \`  /\  |__)  /\  /  \` |  |  /  \ |__) ";
        echo ".__/ |    | | \| | \| | | \| \__>    \__/ |        |  |  | |___    |    |___ \__/ / \    \__, /~~\ |    /~~\ \__, |  |  \__/ |  \ ";
    fi

    if (( $randInt == 1 ))
    then
        echo "      ___  __        ___               __  ___     ___  __             __  ";
        echo "|__/ |__  |__) |\ | |__  |       |\ | /  \  |     |__  /  \ |  | |\ | |  \ ";
        echo "|  \ |___ |  \ | \| |___ |___    | \| \__/  |     |    \__/ \__/ | \| |__/ ";
    fi

   if (( $randInt == 2 ))
    then
        echo " __   ___ ___ ___         __     ___       ___                    __       ___ __ ";
        echo "|__) |__   |   |  | |\ | / _\`     |  |__| |__     |    |  | |    /  \`  /\   |   / ";
        echo "|    |___  |   |  | | \| \__>     |  |  | |___    |___ \__/ |___ \__, /~~\  |  /_ ";
    fi 

    if (( $randInt == 3 ))
    then
        echo " __   ___  __   __   __  ___         __      __   ___             ___     ";
        echo "|__) |__  |__) /  \ /  \  |  | |\ | / _\`    |__) |__   /\  |    |  |  \ / ";
        echo "|  \ |___ |__) \__/ \__/  |  | | \| \__>    |  \ |___ /~~\ |___ |  |   |  ";
    fi 

}